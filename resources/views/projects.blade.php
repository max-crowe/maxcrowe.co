    <h2><a target="_blank" href="https://github.com/mcrowepfx/REST-Client-Framework">REST Client Framework</a></h2>
    <ul class="technologies">
        <li>Python</li>
        <li>REST</li>
    </ul>
    <p>This package attempts to simplify the process of developing clients to connect to third-party REST APIs. In situations requiring the use of multiple REST services, it can be confusing to rely on extant client libraries (if they exist in the first place), since library developers sometimes implement features in idiosyncratic ways. The aim of this package is to make it possible to scaffold a REST API client with a standardized Pythonic interface in quick fashion, while providing the flexibility necessary to adapt to real-world use cases.</p>
    <p>This package was spun off from a project I developed as part of my work with <a target="_blank" href="https://www.performics.com/">Performics</a>. Many thanks to them for helping me give a little bit back to the open-source community.</p>

    <h2><a target="_blank" href="https://github.com/mcrowepfx/Django-Toggled-Widgets">Django Toggled Widgets</a></h2>
    <ul class="technologies">
        <li>Python</li>
        <li>Django</li>
        <li>JavaScript</li>
    </ul>
    <p>I am a big fan of the <a target="_blank" href="https://www.djangoproject.com/">Django</a> web application framework, and I have found its bundled administrative component particularly useful. Sometimes it makes sense for a Django model to have attributes of differing data types that are intended to be set on an exclusive-or basis (i.e., instances of <span class="code">Foo</span> may have a value for the attributes <span class="code">bar</span> or <span class="code">baz</span> but not both). This package provides a convenient UI for this pattern in the Django admin that allows the user to toggle between related fields.</p>
    <p>This was also spun off from my work with <a target="_blank" href="https://www.performics.com/">Performics</a> as an open-source contribution.</p>
